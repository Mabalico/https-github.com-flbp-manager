defineCase('automatic award sync refreshes archived tournament titles while preserving manual overrides', () => {
  const manualOverride: HallOfFameEntry = {
    id: 'arch_2025_top_scorer',
    year: '2025',
    tournamentId: 'arch_2025',
    tournamentName: 'FLBP 2025',
    type: 'top_scorer',
    playerNames: ['Verdi Paolo'],
    playerId: 'verdi_paolo_2001-01-01',
    playerBirthDate: '2001-01-01',
    value: 99,
    sourceType: 'archived_tournament',
    sourceTournamentId: 'arch_2025',
    sourceTournamentName: 'FLBP 2025',
    sourceAutoGenerated: false,
    manuallyEdited: true,
  };
  const manualMvp: HallOfFameEntry = {
    id: 'arch_2025_mvp_rossi',
    year: '2025',
    tournamentId: 'arch_2025',
    tournamentName: 'FLBP 2025',
    type: 'mvp',
    playerNames: ['Rossi Mario'],
    playerId: 'rossi_mario_1998-01-01',
    playerBirthDate: '1998-01-01',
    sourceType: 'archived_tournament',
    sourceTournamentId: 'arch_2025',
    sourceTournamentName: 'FLBP 2025',
    sourceAutoGenerated: false,
    manuallyEdited: true,
  };

  const synced = syncTournamentAwardsToHallOfFame(
    [...baseState.hallOfFame.filter((entry) => entry.tournamentId !== 'arch_2025'), manualOverride, manualMvp],
    tournament,
    tournament.matches || [],
    tournament.teams || []
  );

  const topScorer = synced.find((entry) => entry.id === 'arch_2025_top_scorer');
  const winner = synced.find((entry) => entry.tournamentId === 'arch_2025' && entry.type === 'winner');
  const mvp = synced.find((entry) => entry.id === 'arch_2025_mvp_rossi');
  if (!topScorer || !winner || !mvp) throw new Error('Expected synced award entries.');

  assertEqual(topScorer.playerNames[0], 'Verdi Paolo');
  assertEqual(topScorer.manuallyEdited, true);
  assertEqual(winner.type, 'winner');
  assertEqual(mvp.type, 'mvp');
});

