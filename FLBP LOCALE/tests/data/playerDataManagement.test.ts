defineCase('tournament generation ignores hidden and BYE teams when counting bracket matches', () => {
  const activeA = makeTeam('A', 'Alpha', 'A One', 1998, 'A Two', 1999);
  const activeB = makeTeam('B', 'Bravo', 'B One', 1998, 'B Two', 1999);
  const activeC = makeTeam('C', 'Charlie', 'C One', 1998, 'C Two', 1999);
  const hidden = { ...makeTeam('H', 'Hidden', 'H One', 1998, 'H Two', 1999), hidden: true } as Team;
  const bye = { ...makeTeam('BYE_TEAM', 'BYE Team', 'Bye One', 1998, 'Bye Two', 1999), isBye: true } as Team;

  const { tournament, matches } = generateTournamentStructure(
    [activeA, activeB, activeC, hidden, bye],
    {
      mode: 'elimination',
      tournamentName: 'Test',
    }
  );

  assertEqual(tournament.teams.length, 3);
  assertEqual(matches.filter((match) => match.phase === 'bracket').length, 3);
});

defineCase('tournament generation keeps the selected tournament date', () => {
  const activeA = makeTeam('A', 'Alpha', 'A One', 1998, 'A Two', 1999);
  const activeB = makeTeam('B', 'Bravo', 'B One', 1998, 'B Two', 1999);

  const { tournament } = generateTournamentStructure(
    [activeA, activeB],
    {
      mode: 'elimination',
      tournamentName: 'Test Date',
      startDate: '2026-07-18',
    }
  );

  assertEqual(tournament.startDate, '2026-07-18T00:00:00.000Z');
});

